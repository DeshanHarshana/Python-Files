'''
rstrip() method returns a copy of the string with trailing characters removed 
(based on the string argument passed). If no argument is passed,
 it removes whitespace spaces.
'''
# Python3 program to demonstrate the use of  
# rstrip() method using optional parameters  
  
# string which is to be stripped 
string = "Deshanaaaaaaaaa"
  
# Removes given set of characters from 
# right. 
print(string.strip('a'))  